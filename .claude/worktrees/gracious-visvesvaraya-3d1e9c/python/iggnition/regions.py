# ---------------------------------------------------------------------------
# IgNition-specific position labels (for ML feature construction)
# ---------------------------------------------------------------------------
# Pre-built column-name → (aho_pos, codon_pos) mappings that correspond
# exactly to the wide-format output columns H1…H447 and L1…L444.
#
# Example: column "H1"  → Aho AA position 1, codon position 1
#          column "H3"  → Aho AA position 1, codon position 3
#          column "H4"  → Aho AA position 2, codon position 1
#
# These can be used to build per-position feature DataFrames, apply CDR masks,
# or label training data.

def nt_col_to_aho(col_name: str) -> tuple[int, int]:
    """Convert a wide-format column name ('H42', 'L123') to (aho_pos, codon_pos).

    Returns (aho_position, codon_position) where codon_position ∈ {1, 2, 3}.

    Example::

        nt_col_to_aho("H1")   # → (1, 1)
        nt_col_to_aho("H3")   # → (1, 3)
        nt_col_to_aho("H4")   # → (2, 1)
        nt_col_to_aho("L444") # → (148, 3)
    """
    nt_idx = int(col_name[1:])          # 1-based nt position
    aho_pos = (nt_idx - 1) // 3 + 1    # 1-based Aho AA position
    codon_pos = (nt_idx - 1) % 3 + 1   # 1, 2, or 3
    return aho_pos, codon_pos


def cdr_mask(chain: str, region: str, chain_prefix: str | None = None) -> list[str]:
    """Return the list of IgNition wide-format column names in a CDR/FR region.

    Parameters
    ----------
    chain : str
        ``'H'``, ``'K'``, or ``'L'``.
    region : str
        One of ``'FR1'``, ``'CDR1'``, ``'FR2'``, ``'CDR2'``, ``'FR3'``,
        ``'CDR3'``, ``'FR4'``.
    chain_prefix : str, optional
        Column prefix to use.  Defaults to ``'H'`` for heavy and ``'L'`` for
        kappa/lambda.  (In the wide-format output, both K and L chains use the
        ``L`` prefix.)

    Returns
    -------
    list[str]
        Column names, e.g. ``['H76', 'H77', ..., 'H114']`` for VH CDR1.

    Example::

        cdr_mask("H", "CDR1")    # → ['H76', 'H77', ..., 'H114']
        cdr_mask("L", "CDR3")    # → ['L325', 'L326', ..., 'L414']
    """
    chain = chain.upper()
    if chain not in CDR_REGIONS:
        raise ValueError(f"Unknown chain '{chain}'.")
    aho_start, aho_end = CDR_REGIONS[chain][region]
    nt_start, nt_end = aho_range_to_nt(aho_start, aho_end)
    if chain_prefix is None:
        chain_prefix = "H" if chain == "H" else "L"
    return [f"{chain_prefix}{i}" for i in range(nt_start, nt_end + 1)]
