"""
iggnition.regions — Structural and functional region annotations for antibody
variable domains, expressed in the Aho coordinate system used by IgNition.

All positions are 1-based.  Two coordinate frames are provided:

  * **Aho AA** – amino acid position in the Aho numbering scheme
    (H: 1–149, K/L: 1–148).
  * **IgNition nt** – the corresponding nucleotide positions as produced by
    ``iggnition.run()``.  The relationship is exact and deterministic::

        nt_start(aho) = (aho - 1) * 3 + 1
        nt_end(aho)   =  aho      * 3

Derivation
----------
CDR/FR boundaries, conserved-residue positions, and insertion-slot locations
were derived directly from the human germline database embedded in IgNition
(``src/core/germline_data.rs``, generated by ``scripts/bootstrap_germlines.py``
from ANARCI's ``number_aho()`` function).

* CDR/FR boundaries: determined by gap patterns across all IGHV/IGKV/IGLV
  germlines — insertion-slot positions (occupied by some germlines, absent in
  others) define the outer edges of each CDR.
* Conserved residues (Cys23, Trp43, Cys106): read directly from
  representative germlines (IGHV1-18*01, IGKV1-12*01, IGLV1-36*01).
* VH ↔ IMGT correspondence at anchor positions:
  – Aho 23 = IMGT 23 (FR1 Cys)
  – Aho 43 = IMGT 41 (FR2 Trp)  → +2 offset post-CDR1
  – Aho 106 = IMGT 104 (FR3 Cys) → +2 offset post-CDR2

References
----------
* Honegger & Plückthun (2001) J Mol Biol 309:657 — Aho numbering scheme.
* Foote & Winter (1992) J Mol Biol 224:487 — Vernier zone.
* Chothia et al. (1989) Nature 342:877; (1992) J Mol Biol 227:799 — canonical
  loop structures.
* Al-Lazikani et al. (1997) J Mol Biol 273:927 — canonical structure analysis.
* Lefranc et al. (2003) Dev Comp Immunol 27:55 — IMGT numbering.
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# Helper
# ---------------------------------------------------------------------------

def aho_to_nt(aho_pos: int) -> tuple[int, int, int]:
    """Return the three IgNition nt positions for an Aho AA position."""
    base = (aho_pos - 1) * 3
    return base + 1, base + 2, base + 3


def aho_range_to_nt(aho_start: int, aho_end: int) -> tuple[int, int]:
    """Return the (first_nt, last_nt) IgNition nt range for an Aho AA range."""
    return (aho_start - 1) * 3 + 1, aho_end * 3


def region_of(aho_pos: int, chain: str) -> str | None:
    """Return the CDR/FR name for a given Aho position and chain ('H', 'K', or 'L').

    Returns ``None`` if the position is an insertion slot that does not belong
    to any canonical region (this should not occur for standard sequences).
    """
    chain = chain.upper()
    if chain not in CDR_REGIONS:
        raise ValueError(f"Unknown chain '{chain}'. Expected 'H', 'K', or 'L'.")
    for name, (start, end) in CDR_REGIONS[chain].items():
        if start <= aho_pos <= end:
            return name
    return None


